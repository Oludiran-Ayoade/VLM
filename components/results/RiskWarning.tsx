"use client";

interface RiskWarningProps {
  warning: string;
  confidenceLevel: string;
}

export function RiskWarning({ warning, confidenceLevel }: RiskWarningProps) {
  const getConfidenceStyle = () => {
    switch (confidenceLevel) {
      case "High":
        return "text-emerald-400 bg-emerald-500/10 border-emerald-500/20";
      case "Medium":
        return "text-amber-400 bg-amber-500/10 border-amber-500/20";
      case "Low":
        return "text-red-400 bg-red-500/10 border-red-500/20";
      default:
        return "text-white/50 bg-white/5 border-white/10";
    }
  };

  return (
    <div className="rounded-2xl border border-white/10 bg-white/[0.02] p-6 space-y-5">
      {/* Header */}
      <div className="flex items-center justify-between">
        <div className="flex items-center gap-3">
          <div className="w-8 h-8 rounded-lg bg-white/10 flex items-center justify-center">
            <span className="text-base">🛡️</span>
          </div>
          <h3 className="text-base font-semibold text-white">Risk Assessment</h3>
        </div>
        <span className={`text-xs font-medium px-3 py-1.5 rounded-full border ${getConfidenceStyle()}`}>
          {confidenceLevel} Confidence
        </span>
      </div>

      {/* Warning text */}
      <p className="text-sm text-white/60 leading-relaxed">{warning}</p>

      {/* Disclaimer */}
      <div className="pt-4 border-t border-white/5">
        <p className="text-xs text-white/30 leading-relaxed">
          <span className="text-white/40 font-medium">Disclaimer:</span> This analysis
          is generated by an AI model and should not be considered financial
          advice. Always conduct your own research and use proper risk
          management. Past chart patterns do not guarantee future results.
          Trading foreign exchange carries significant risk of loss.
        </p>
      </div>
    </div>
  );
}
